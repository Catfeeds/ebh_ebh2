        init:function (data) {
            this.last = data || new Date().getTime() + "";
            this.oWrap = $("#wrap");
            this.timer = null;
            //
            transfer.render(this.last);
            //
            transfer.auto();
        },
        getDiff:function () {
            var arr = this.last.split("");
            var arr2 = this.iNow.split("");
            var arr3 = [];
            for (var i = 0; i < arr2.length; i++) {
                if (arr[i] != arr2[i]) {
                    arr3.push({"index":i, "val":arr2[i]});
                }
            }
            this.last = this.iNow;
            return arr3;
        },
        render:function (str) {
            var arr = str.split("");
            var len = arr.length;
            var frag = '';
            for (var i = 0; i < len; i++) {
                frag += "<div class='test'>" + arr[i] + "</div>";
            }
            this.oWrap.html(frag);
            this.el = this.oWrap.children();
        },
        auto:function () {
            var _this = this;
            clearInterval(this.timer);
            this.timer = setInterval(function () {
                var data = transfer.getDiff();
                $.each(data, function (i, el) {
                    _this.el.eq(el.index).addClass("animation");
                    setTimeout(function () {
                        _this.el.eq(el.index).removeClass("animation").html(el.val);
                    }, 380);
                });
            }, 4000);
        },
        setData:function (data) {
            this.iNow = data;
        }
    };
